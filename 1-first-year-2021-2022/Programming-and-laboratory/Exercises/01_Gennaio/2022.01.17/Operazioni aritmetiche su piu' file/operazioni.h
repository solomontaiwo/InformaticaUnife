#include <stdio.h>

int addizione(int numero1, int numero2)
{
    return numero1 + numero2;
}

int sottrazione(int numero1, int numero2)
{
    return numero1 - numero2;
}

int moltiplicazione(int numero1, int numero2)
{
    return numero1 * numero2;
}

float divisione(float numero1, float numero2)
{
    return numero1 / numero2;
}
