package Java.esempioMath;

public class esempioMath {
    public static void main(String args[]) {
        double x, y;
        x = 5;
        y = Math.log(x);
        System.out.println("Il logaritmo di " + x + " e' " + y);
        x = Math.PI / 2;
        y = Math.sin(x);
        System.out.println("Il seno di " + x + " e' " + y);
    }
}