public class Squadra {
	protected int codice;
	protected String sport, nomeSquadra;
	protected int vinte, perse;

	public Squadra(int codice, String sport, String nomeSquadra, int vinte, int perse) {
		this.codice = codice;
		this.sport = sport;
		this.nomeSquadra = nomeSquadra;
		this.vinte = vinte;
		this.perse = perse;
	}
}